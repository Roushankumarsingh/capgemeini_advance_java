package Assessment;
import javax.persistence.*;

@Entity 

public class Subject {
	@Id 
	int id ; 
	String name ; 
	int days ;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getDays() {
		return days;
	}
	public void setDays(int days) {
		this.days = days;
	} 
}
